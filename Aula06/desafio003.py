# Questão: Crie um programa que leia dois números e mostre a soma entre eles

# Observação: Coloquei os dois número sendo do tipo float para que seja aceito valores decimais para serem somados 

num1 = (float(input('Digite um número: ')))

num2 = (float(input('Digite outro número: ')))

soma = num1 + num2

print(f'A soma entre o número 1: {num1} e o número 2: {num2} eh: {soma} ')