# ---------------------- Uitlização de Variáveis: ----------------------
# 1. Variável no python nada mais é do que um objeto que recebe valor, podendo ser qualquer tipo de valor

# A variável 'nome' recebeu o valor 'Alan' que é interpretada do tipo string(texto) 
nome = 'Alan'

# A variável 'idade' recebeu o valor '21' que é interpretada do tipo integer(número inteiro) 
idade = 21

# A variável 'peso' recebeu o valor '74.8' que é interpretada do tipo float(número ponto flutuante). Obs: Para adicionar número com ponto flutuante é necessário adicionar o "." e não a "," .
peso = 74.8

# Para exitir essas informações todas juntas é necessário utilizar a "," e não o operador "+", pois estamos seperando e utilizando variáveis com tipos diferentes 
print(nome, idade, peso)