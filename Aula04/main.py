# ---------------------- Formatos de imprimir valores ----------------------

# 1. Padrão com texto
print('Olá, Mundo!')

# 2. Teste com uma soma
print(7+4)

# 3. Testando o erro de uma junção contendo tipos diferentes, assim o operador "+" só junta(soma) variáveis e valores do mesmo tipo
print('Olá' + 5)

# 4. Para corrigir o erro apresentado no tópico 3, deve-se modificar o operador "+" pela ",". Assim o resultado é: 'Olá 5'
print('Olá' , 5)


